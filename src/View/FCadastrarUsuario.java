    private void initializeTable() {
        modelo = new DefaultTableModel();
        jTUsuario.setModel(modelo);
        String[] colunas = {"Usuário", "Senha", "Tipo", "Ativado"};
        modelo.setColumnIdentifiers(colunas);

        try {
            for (Usuarios usuario : usuarioDAO.listaUsuarios()) {
                Object[] linha = {usuario.getUsuario(), usuario.getSenha(), usuario.getTipo(), usuario.isAtivado()};
                modelo.addRow(linha);
            }
        } catch (SQLException e) {
            System.err.println("Erro ao carregar Usuários do banco de dados: " + e.getMessage());
        }
    }

