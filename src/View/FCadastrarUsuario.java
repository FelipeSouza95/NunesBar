    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String usuario = txtUsuario.getText();
        String senha = new String(txtPass.getPassword());
        String tipo = boxCategoria.getSelectedItem().toString();
        boolean ativado = btnAtivado.isSelected();

        try {
            Usuarios novoUsuario = new Usuarios(usuario, senha, tipo, ativado);
            usuarioDAO.adicionarUsuario(novoUsuario);

            JOptionPane.showMessageDialog(this, "Usuário adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            limparCampos();
            atualizarTabela();

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao adicionar usuário: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int linhaSelecionada = jTUsuario.getSelectedRow();
        if (linhaSelecionada >= 0) {
            int idUsuario = (int) modelo.getValueAt(linhaSelecionada, 0); // Obtém o ID do usuário da linha selecionada

            try {
                usuarioDAO.excluirUsuario(idUsuario); // Exclui o usuário pelo ID

                JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

                atualizarTabela();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir usuário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um usuário para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int selectedRow = jTUsuario.getSelectedRow();
        if (selectedRow != -1) {
            // Obtém os novos dados do usuário dos campos de entrada
            String usuario = txtUsuario.getText();
            String senha = new String(txtPass.getPassword());
            String tipo = boxCategoria.getSelectedItem().toString();
            boolean ativado = btnAtivado.isSelected();

            try {
                // Obtém o usuário selecionado da tabela
                Usuarios usuarioSelecionado = usuarioDAO.listaUsuarios().get(selectedRow);

                // Atualiza os dados do usuário selecionado
                usuarioSelecionado.setUsuario(usuario);
                usuarioSelecionado.setSenha(senha);
                usuarioSelecionado.setTipo(tipo);
                usuarioSelecionado.setAtivado(ativado);

                // Chama o método para atualizar o usuário no banco de dados
                usuarioDAO.atualizarUsuario(usuarioSelecionado);

                // Exibe uma mensagem de sucesso
                JOptionPane.showMessageDialog(this, "Usuário alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

                // Atualiza a tabela após alterar o usuário
                atualizarTabela();

            } catch (SQLException ex) {
                // Exibe uma mensagem de erro em caso de falha
                JOptionPane.showMessageDialog(this, "Erro ao atualizar usuário: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um usuário para alterar.");
        }

        // Limpa os campos após a operação
        limparCampos();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void limparCampos() {
        txtUsuario.setText("");
        txtPass.setText("");
        boxCategoria.setSelectedIndex(0);
        btnAtivado.setSelected(false);
        btnDesativado.setSelected(false);
    }

    private void atualizarTabela() {
        modelo.setRowCount(0); // Limpa a tabela
        try {
            for (Usuarios usuario : usuarioDAO.listaUsuarios()) {
                Object[] linha = {usuario.getUsuario(), usuario.getSenha(), usuario.getTipo(), usuario.isAtivado()};
                modelo.addRow(linha);
            }
        } catch (SQLException e) {
            System.err.println("Erro ao carregar Usuários do banco de dados: " + e.getMessage());
        }
    }

